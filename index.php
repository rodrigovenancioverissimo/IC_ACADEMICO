    function ajustar_width(str) {
      var elements = document.getElementsByClassName(str);
      for (let i = 0; i < elements.length; i++){
        var el = elements[i];
        var x = 0;
        el.style.width = '';
        x = el.offsetWidth;
        if(x > 305){
          el.style.width = (x - (x % 305)) + 'px';
        }else{
          el.style.width = '100%';
        }
      }
    };
    window.onresize = function(event) {
      ajustar_width('card-columns');
    };
    ajustar_width('card-columns');
